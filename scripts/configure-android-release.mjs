import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { resolve } from 'node:path'

const repoRoot = process.cwd()
const tauriConfigPath = resolve(repoRoot, 'desktop/src-tauri/tauri.conf.json')
const androidAppDir = resolve(repoRoot, 'desktop/src-tauri/gen/android/app')
const tauriPropertiesPath = resolve(androidAppDir, 'tauri.properties')
const gradleBuildPath = resolve(androidAppDir, 'build.gradle.kts')

if (!existsSync(tauriConfigPath)) {
  throw new Error(`Missing Tauri config: ${tauriConfigPath}`)
}

if (!existsSync(gradleBuildPath)) {
  throw new Error(`Missing generated Android build.gradle.kts: ${gradleBuildPath}`)
}

const tauriConfig = JSON.parse(readFileSync(tauriConfigPath, 'utf8'))
const version = String(tauriConfig.version ?? '').trim()
if (!version) {
  throw new Error('desktop/src-tauri/tauri.conf.json is missing a version')
}

const explicitVersionCode = tauriConfig.bundle?.android?.versionCode
const versionCode =
  typeof explicitVersionCode === 'number' && Number.isFinite(explicitVersionCode)
    ? explicitVersionCode
    : deriveVersionCode(version)

const nextProperties = [
  '// THIS IS AN AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY.',
  `tauri.android.versionName=${version}`,
  `tauri.android.versionCode=${versionCode}`,
  '',
].join('\n')
mkdirSync(androidAppDir, { recursive: true })
writeFileSync(tauriPropertiesPath, nextProperties)
console.log(`Synced ${tauriPropertiesPath}`)

const signingPreludeBlock = `// CLAW_QUEST_RELEASE_SIGNING_START
val keystorePropertiesFile = rootProject.file("keystore.properties")
val keystoreProperties = Properties().apply {
    if (keystorePropertiesFile.exists()) {
        FileInputStream(keystorePropertiesFile).use { load(it) }
    }
}
val hasReleaseKeystore =
    keystoreProperties["keyAlias"] != null &&
    keystoreProperties["keyPassword"] != null &&
    keystoreProperties["storePassword"] != null &&
    keystoreProperties["storeFile"] != null
// CLAW_QUEST_RELEASE_SIGNING_END`

const signingConfigsBlock = `    // CLAW_QUEST_SIGNING_CONFIGS_START
    signingConfigs {
        if (hasReleaseKeystore) {
            create("release") {
                keyAlias = keystoreProperties["keyAlias"] as String
                keyPassword = keystoreProperties["keyPassword"] as String
                storeFile = file(keystoreProperties["storeFile"] as String)
                storePassword = keystoreProperties["storePassword"] as String
            }
        }
    }
    // CLAW_QUEST_SIGNING_CONFIGS_END`

const releaseSigningLine = `            // CLAW_QUEST_RELEASE_BUILD_TYPE_START
            if (hasReleaseKeystore) {
                signingConfig = signingConfigs.getByName("release")
            }
            // CLAW_QUEST_RELEASE_BUILD_TYPE_END`

let gradleBuild = readFileSync(gradleBuildPath, 'utf8')

if (!gradleBuild.includes('import java.io.FileInputStream')) {
  gradleBuild = replaceOnceOrThrow(
    gradleBuild,
    /^import java\.util\.Properties$/m,
    'import java.io.FileInputStream\nimport java.util.Properties',
    'insert FileInputStream import',
  )
}

if (!gradleBuild.includes('CLAW_QUEST_RELEASE_SIGNING_START')) {
  gradleBuild = replaceOnceOrThrow(
    gradleBuild,
    /(val tauriProperties = Properties\(\)\.apply \{[\s\S]*?\n\})/,
    `$1\n\n${signingPreludeBlock}`,
    'insert signing prelude block',
  )
}

if (!gradleBuild.includes('CLAW_QUEST_SIGNING_CONFIGS_START')) {
  gradleBuild = replaceOnceOrThrow(
    gradleBuild,
    /\n(\s*)buildTypes\s*\{/,
    `\n${signingConfigsBlock}\n$1buildTypes {`,
    'insert signingConfigs block',
  )
}

if (!gradleBuild.includes('CLAW_QUEST_RELEASE_BUILD_TYPE_START')) {
  gradleBuild = replaceOnceOrThrow(
    gradleBuild,
    /(getByName\("release"\)\s*\{\s*\n)/,
    `$1${releaseSigningLine}\n`,
    'insert release signing config hookup',
  )
}

assertContains(gradleBuild, 'CLAW_QUEST_RELEASE_SIGNING_START', 'Missing signing prelude marker')
assertContains(gradleBuild, 'CLAW_QUEST_SIGNING_CONFIGS_START', 'Missing signingConfigs marker')
assertContains(gradleBuild, 'CLAW_QUEST_RELEASE_BUILD_TYPE_START', 'Missing release build type marker')
assertContains(gradleBuild, 'signingConfig = signingConfigs.getByName("release")', 'Missing release signing assignment')

writeFileSync(gradleBuildPath, gradleBuild)
console.log(`Configured ${gradleBuildPath}`)

function replaceOnceOrThrow(source, pattern, replacement, reason) {
  const next = source.replace(pattern, replacement)
  if (next === source) {
    throw new Error(`Could not ${reason}`)
  }
  return next
}

function assertContains(source, snippet, message) {
  if (!source.includes(snippet)) {
    throw new Error(message)
  }
}

function deriveVersionCode(version) {
  const match = version.match(/^(\d+)\.(\d+)\.(\d+)$/)
  if (!match) {
    throw new Error(`Unsupported semver version for Android versionCode derivation: ${version}`)
  }

  const [, major, minor, patch] = match.map(Number)
  return major * 1_000_000 + minor * 1_000 + patch
}
